// codexHooks declares the one optional, detect-only lifecycle hook. Codex does
// not trust a plugin's hooks until the user reviews and accepts them, so the
// plugin is fully usable with the hook declined — it only turns "af: command
// not found" three turns in into one clear line at session start.
func codexHooks() map[string]any {
	return map[string]any{
		"hooks": map[string]any{
			"SessionStart": []any{
				map[string]any{
					"hooks": []any{
						map[string]any{
							"type":          "command",
							"command":       `bash "${PLUGIN_ROOT}/hooks/af-preflight.sh"`,
							"statusMessage": "Checking for the af CLI",
						},
					},
				},
			},
		},
	}
}

// afPreflightHook reports whether af is on PATH, and nothing else.
//
// It deliberately does NOT download or install anything. A plugin hook runs as
// the user, and af's releases carry no checksum or signature to verify against
// (install.sh and `af upgrade` both check only that the tarball is a tarball),
// so fetch-and-execute from inside an agent session is the wrong shape at any
// convenience — see #2172 and #2174. Detect and instruct.
func afPreflightHook() string {
	return "#!/usr/bin/env bash\n" +
		"# " + pluginGenBanner + "\n" +
		"#\n" +
		"# Report whether the af CLI is on PATH. Read-only on purpose: this hook never\n" +
		"# downloads or installs anything (see docs/agent-plugins.md).\n" +
		"set -euo pipefail\n" +
		"\n" +
		"if command -v af >/dev/null 2>&1; then\n" +
		"\t# `af version` can print a second \"an upgrade is available\" line; the\n" +
		"\t# hook only wants the version itself.\n" +
		"\tversion=$(af version 2>/dev/null | head -n 1)\n" +
		"\techo \"${version:-af (version unknown)} is available.\"\n" +
		"else\n" +
		"\techo \"af is not installed. Install it with:\"\n" +
		"\techo \"  " + session.AfInstallCommand + "\"\n" +
		"fi\n"
}

