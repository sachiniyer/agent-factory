// afSkillMarkdown is the SKILL.md every agent's plugin carries: name +
// description frontmatter (the only fields codex, claude, gemini and amp all
// require) over the plugin-framed af usage reference.
//
// The body is session.AfPluginUsageReference, never afUsageReference: the
// canonical text opens with "You are running inside Agent Factory (af)", which
// is false for someone who installed this plugin into an agent af did not
// launch. session/systemprompt.go swaps the framing and keeps the body shared;
// TestGeneratedSkillIsReframed locks that the false opening never ships here.
func afSkillMarkdown() string {
	return "---\n" +
		"name: " + session.AfSkillName + "\n" +
		"description: " + session.AfSkillDescription + "\n" +
		"---\n" +
		"<!-- " + pluginGenBanner + " -->\n" +
		"\n" +
		session.AfPluginUsageReference + "\n"
}

// pluginAuthor is the shared author/owner block.
type pluginAuthor struct {
	Name string `json:"name"`
	URL  string `json:"url,omitempty"`
}

func afPluginAuthor() pluginAuthor {
	return pluginAuthor{Name: session.AfPluginAuthorName, URL: session.AfPluginHomepage}
}

// pluginKeywords are the discovery tags every manifest carries.
func pluginKeywords() []string {
	return []string{"agents", "worktrees", "tmux", "automation", "sessions"}
}

// ---------------------------------------------------------------- codex

// codexManifest is `.codex-plugin/plugin.json`. Field set and path conventions
// are from the Codex plugin manifest reference (verified against codex-cli
// 0.144.6, which installs this manifest as written): relative component paths
// begin with "./", only plugin.json belongs in .codex-plugin/, and skills/,
// hooks/ and assets/ sit at the plugin root.
type codexManifest struct {
	Name        string         `json:"name"`
	Version     string         `json:"version"`
	Description string         `json:"description"`
	Author      pluginAuthor   `json:"author"`
	Homepage    string         `json:"homepage"`
	Repository  string         `json:"repository"`
	License     string         `json:"license"`
	Keywords    []string       `json:"keywords"`
	Skills      string         `json:"skills"`
	Hooks       string         `json:"hooks"`
	Interface   codexInterface `json:"interface"`
}

// codexInterface is the manifest's presentation block. defaultPrompt is capped
// at three entries of at most 128 characters by Codex; entries past the third
// are dropped and longer ones truncated, so keep them short.
type codexInterface struct {
	DisplayName      string   `json:"displayName"`
	ShortDescription string   `json:"shortDescription"`
	LongDescription  string   `json:"longDescription"`
	DeveloperName    string   `json:"developerName"`
	Category         string   `json:"category"`
	WebsiteURL       string   `json:"websiteURL"`
	DefaultPrompt    []string `json:"defaultPrompt"`
}

// codexMarketplace is `.agents/plugins/marketplace.json` — the repo-scoped
// catalog a user adds with `codex plugin marketplace add sachiniyer/agent-factory`.
type codexMarketplace struct {
	Name      string                    `json:"name"`
	Interface codexMarketplaceInterface `json:"interface"`
	Plugins   []codexMarketplacePlugin  `json:"plugins"`
}

type codexMarketplaceInterface struct {
	DisplayName string `json:"displayName"`
}

type codexMarketplacePlugin struct {
	Name     string                 `json:"name"`
	Source   codexMarketplaceSource `json:"source"`
	Policy   codexMarketplacePolicy `json:"policy"`
	Category string                 `json:"category"`
}

// codexMarketplaceSource uses the "local" source kind: the plugin lives in the
// same repo as the marketplace, and Codex resolves the path against the
// marketplace root (the directory holding .agents/). The path must begin with
// "./" and stay inside that root.
type codexMarketplaceSource struct {
	Source string `json:"source"`
	Path   string `json:"path"`
}

type codexMarketplacePolicy struct {
	Installation   string `json:"installation"`
	Authentication string `json:"authentication"`
}

// codexPluginRoot is the Codex plugin's root, relative to the repo root. The
// leaf is the plugin name because Codex's marketplace convention is that the
