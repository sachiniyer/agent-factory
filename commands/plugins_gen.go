// pluginsReadme is the generated index for plugins/. It is generated too, so
// the agent table and the install instructions cannot disagree.
func pluginsReadme() string {
	var b strings.Builder
	b.WriteString("<!-- " + pluginGenBanner + " -->\n\n")
	b.WriteString("# Agent plugins\n\n")
	b.WriteString("Installable packagings of one thing: the `" + session.AfSkillName +
		"` skill that teaches an AI coding agent to drive the `af` CLI. Install the one " +
		"for your agent and it can run and schedule af sessions in any repo — including " +
		"sessions af did not launch.\n\n")
	b.WriteString("Every file in this directory is **generated** from " +
		"`session.AfPluginUsageReference`, the same af usage guidance af injects into " +
		"the agents it launches itself. Edit `session/systemprompt.go` and run " +
		"`scripts/gen-docs.sh`; hand-edits here are reverted and fail CI.\n\n")
	b.WriteString("The plugin does **not** install `af`. Install it first:\n\n")
	b.WriteString("```\n" + session.AfInstallCommand + "\n```\n\n")

	for _, a := range pluginAgents {
		b.WriteString("## " + a.name + "\n\n")
		b.WriteString(a.packaging + "\n\n")
		if a.install != "" {
			b.WriteString("```\n" + a.install + "\n```\n\n")
		}
	}

	b.WriteString("See [docs/agent-plugins.md](../docs/agent-plugins.md) for what each " +
		"packaging does and does not do.\n")
	return b.String()
}

// generatedPluginFiles returns every generated artifact with a version bound to
// the complete version-neutral tree. The ordered release serial makes every
// later release greater under SemVer; the pinned digest makes generation fail
// until a payload change advances that serial (#2179/#2243 review).
func generatedPluginFiles() []pluginFile {
	seed := generatedPluginFilesAtVersion(pluginVersionSeed)
	return generatedPluginFilesAtVersion(pluginContentVersion(seed))
}

// generatedPluginFilesAtVersion is the single enumeration the content-version
// pass, writer, pruner, and drift test read.
func generatedPluginFilesAtVersion(version string) []pluginFile {
	files := []pluginFile{{path: pluginsDir + "/README.md", content: pluginsReadme()}}
	for _, a := range pluginAgents {
		files = append(files, a.files(version)...)
	}
	for i := range files {
		if files[i].mode == 0 {
			files[i].mode = 0o644
		}
	}
	sort.Slice(files, func(i, j int) bool { return files[i].path < files[j].path })
	return files
}

// pluginContentVersion validates the complete seeded tree against the release
// pin, then gives it a monotonic SemVer identity. Minor is the ordered release
// serial; patch plus build metadata retain a 64-bit content address. A payload
// change without an explicit serial bump panics before any generated file is
// written, turning the easy-to-forget cache invariant into a hard generation
// gate rather than another maintainer convention.
func pluginContentVersion(files []pluginFile) string {
	sum := pluginContentSum(files)
	digest := fmt.Sprintf("%x", sum)
	release := uint64(len(pluginReleaseDigests))
	if release == 0 || digest != pluginReleaseDigests[len(pluginReleaseDigests)-1] {
		panic(fmt.Sprintf(
			"plugin generator: release %d content digest is %s; append it to pluginReleaseDigests (do not replace existing entries) to advance the plugin version",
			release, digest,
		))
	}
	return pluginVersionForRelease(release, sum)
}

func pluginContentSum(files []pluginFile) [sha256.Size]byte {
	h := sha256.New()
	for _, f := range files {
		_, _ = fmt.Fprintf(h, "%s\x00%o\x00%s\x00", f.path, f.mode, f.content)
	}
	var sum [sha256.Size]byte
	copy(sum[:], h.Sum(nil))
	return sum
}

func pluginVersionForRelease(release uint64, sum [sha256.Size]byte) string {
	return fmt.Sprintf("%d.%d.%d+sha256.%x",
		pluginVersionMajor,
		release,
		binary.BigEndian.Uint32(sum[:4]),
		sum[:8],
	)
}

// mustJSON renders a manifest as indented JSON with a trailing newline. The
// input is always a literal built above, so a marshal failure is a programming
// error, not a runtime condition.
func mustJSON(v any) string {
	out, err := json.MarshalIndent(v, "", "  ")
	if err != nil {
		panic(fmt.Sprintf("plugin generator: marshaling %T: %v", v, err))
	}
	return string(out) + "\n"
}

// writeAgentPlugins writes every generated artifact under root and prunes any
// stale file left under plugins/ by an earlier revision of the table, so
// deleting an agent removes its artifacts instead of orphaning them (the same
// property session.ensurePluginDir gives the runtime plugin dir).
//
// root must be THIS af checkout — the go.mod module directive is validated
// before the first write. The pruner deletes files under plugins/ only; the two
// marketplace files are named, single, and overwritten in place.
func writeAgentPlugins(root string) ([]string, error) {
	goModPath := filepath.Join(root, "go.mod")
	goMod, err := os.ReadFile(goModPath)
	if err != nil {
		return nil, fmt.Errorf("--plugin-root %q is not an af checkout (cannot read go.mod): %w", root, err)
	}
	module := goModulePath(goMod)
	if module != agentFactoryModule {
		return nil, fmt.Errorf("--plugin-root %q is not an af checkout (go.mod module is %q, want %q)", root, module, agentFactoryModule)
	}

	files := generatedPluginFiles()
	written := make(map[string]bool, len(files))
	paths := make([]string, 0, len(files))

	for _, f := range files {
		abs := filepath.Join(root, filepath.FromSlash(f.path))
		if err := os.MkdirAll(filepath.Dir(abs), 0o755); err != nil {
			return nil, fmt.Errorf("failed to create %s: %w", filepath.Dir(abs), err)
		}
		if err := os.WriteFile(abs, []byte(f.content), f.mode); err != nil {
			return nil, fmt.Errorf("failed to write %s: %w", abs, err)
		}
		// WriteFile does not change the mode of an existing file, so a mode
		// change (or a file that arrived with the wrong bits) is applied
		// explicitly — the preflight hook has to stay executable.
		if err := os.Chmod(abs, f.mode); err != nil {
			return nil, fmt.Errorf("failed to chmod %s: %w", abs, err)
		}
		written[abs] = true
		paths = append(paths, f.path)
	}

	if err := prunePluginTree(filepath.Join(root, pluginsDir), written); err != nil {
		return nil, err
	}
	return paths, nil
}

// goModulePath reads the module directive without accepting a mere go.mod as
// ownership proof. Quoted module paths are legal, though this repo uses the
// ordinary unquoted form.
func goModulePath(goMod []byte) string {
	for _, line := range strings.Split(string(goMod), "\n") {
		fields := strings.Fields(line)
		if len(fields) < 2 || fields[0] != "module" {
			continue
		}
		return strings.Trim(fields[1], `"`)
	}
	return ""
}

// prunePluginTree removes every file under dir that the generator did not just
// write, then removes the directories left empty. A missing dir is not an error
// (nothing generated yet).
func prunePluginTree(dir string, written map[string]bool) error {
	var dirs []string
	err := filepath.Walk(dir, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			dirs = append(dirs, path)
			return nil
		}
		if written[path] {
			return nil
		}
		return os.Remove(path)
	})
	if os.IsNotExist(err) {
		return nil
	}
	if err != nil {
		return fmt.Errorf("failed to prune stale plugin artifacts under %s: %w", dir, err)
	}
	// Deepest first, so a directory emptied by the walk above is removed too.
	sort.Sort(sort.Reverse(sort.StringSlice(dirs)))
	for _, d := range dirs {
		if d == dir {
			continue
		}
		_ = os.Remove(d) // fails harmlessly (ENOTEMPTY) when still in use
	}
	return nil
}
