// afSkillMarkdown is the SKILL.md every agent's plugin carries: name +
// description frontmatter (the only fields codex, claude, gemini and amp all
// require) over the plugin-framed af usage reference.
//
// The body is session.AfPluginUsageReference, never afUsageReference: the
// canonical text opens with "You are running inside Agent Factory (af)", which
// is false for someone who installed this plugin into an agent af did not
// launch. session/systemprompt.go swaps the framing and keeps the body shared;
// TestGeneratedSkillIsReframed locks that the false opening never ships here.
func afSkillMarkdown() string {
	return "---\n" +
		"name: " + session.AfSkillName + "\n" +
		"description: " + session.AfSkillDescription + "\n" +
		"---\n" +
		"<!-- " + pluginGenBanner + " -->\n" +
		"\n" +
		session.AfPluginUsageReference + "\n"
}

