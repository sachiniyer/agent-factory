// This file generates the installable per-agent plugin artifacts committed to
// the repo (#2172): the Codex plugin and its marketplace, the Claude Code
// plugin and its marketplace, and the portable skill directories for gemini and
// amp. Every one of them carries the SAME af usage guidance —
// session.AfPluginUsageReference, the plugin-framed rendering of the canonical
// afUsageReference — so a user who installs the plugin for their agent gets
// exactly what af injects into the agents it launches itself, and no copy can
// drift from the source.
//
// It lives in commands/ rather than session/ for one reason: the generator is
// wired into `af gen-docs`, which walks the live Cobra tree, and session/
// deliberately imports nothing but config/log. commands/ may import session to
// read the text; session/ may not import the command tree.
//
// Everything under plugins/ and both marketplace files are OUTPUT. Hand-edits
// are reverted by the next `scripts/gen-docs.sh` and fail the CI drift gate
// (.github/workflows/docs.yml) and TestGeneratedPluginsAreCommitted.

const (
	// pluginVersionSeed breaks the version/content cycle while deriving the real
	// manifest version. Hash the complete tree rendered with this fixed seed,
	// then render once more with the content-derived version (#2179 review).
	pluginVersionSeed = "0.0.0"

	// Major 3 sorts after the unordered 2.<hash>.<hash> scheme shipped by #2243.
	pluginVersionMajor = 3

	// agentFactoryModule is the ownership proof writeAgentPlugins requires before
	// its first write or prune. A random directory containing any go.mod is not an
	// af checkout and must never become a destructive target (#2179 review).
	agentFactoryModule = "github.com/sachiniyer/agent-factory"

	// pluginsDir is the committed output root for the per-agent plugins,
	// relative to the repo root.
	pluginsDir = "plugins"

	// pluginLicense mirrors LICENSE.md.
	pluginLicense = "AGPL-3.0"

	// pluginCategory is the marketplace display bucket both Codex and the
	// generated README use.
	pluginCategory = "Developer Tools"
)

// pluginReleaseDigests is an append-only release ledger. Its length is the
// monotonic SemVer minor: a seeded payload/path/mode change must append the new
// digest, which advances ordering automatically. pluginContentVersion refuses
// to generate when the final entry does not match, so the ordinary fix for a
// changed payload cannot update its identity without also moving forward.
var pluginReleaseDigests = []string{
	"951d1d7a412f6c4a28b9498c7b16bda79ff6fad12c5b40963b6cf68f637fcf91", // 3.1
	"d5bf3050bea0bcdbcd41e436738b56d2dd4e25e82b508a751e3c2d1435473f09", // 3.2
	"fb450d6ee2a3cf923ace4ef7dd693b5d9790bb52ec33328bc242fea910f0be95", // 3.3 — instance/session/tab/pane model + dispatched-parent escalation + tightened web-tab guidance (#2473)
}

// pluginGenBanner marks a generated Markdown/shell artifact. Like genBanner it
// carries no version or timestamp, so regenerating an unchanged tree is
// byte-identical — the property the drift gate depends on. JSON manifests get no
// banner: they have no comment syntax, and both Codex and Claude Code validate
// the manifest against a closed field set, so an invented "_generated" key is a
// real risk for no gain.
const pluginGenBanner = "Generated by `af gen-docs --plugin-root` — DO NOT EDIT. " +
	"Regenerate with scripts/gen-docs.sh."

// pluginFile is one generated artifact, addressed relative to the repo root.
type pluginFile struct {
	path    string // slash-separated, relative to the repo root
	content string
	mode    os.FileMode
}

// pluginAgent is one agent's packaging of the af skill. Adding an agent is a
// new entry in pluginAgents: name it, say how a user installs it, and return
// the files. The skill text itself is never per-agent — afSkillMarkdown is
// shared, so a new agent cannot introduce a fifth copy of the usage guidance.
type pluginAgent struct {
	// name is the agent as af names it elsewhere (config program names).
	name string
	// packaging is one line for the generated README: how a user installs it.
	packaging string
	// install is the copy-pasteable install command(s) for the README, or ""
	// when the packaging is a plain copy documented in packaging.
	install string
	// files returns every artifact this agent owns, repo-root-relative.
	files func(version string) []pluginFile
}

